class BigcommentCommand(sublime_plugin.TextCommand):
	def run(self, edit):
		for region in self.view.sel():
			if not region.empty():
				s = self.view.substr(region)

				lines = ["", "", "", "", "", "", ""]

				for chh in s:
					ch = chh.lower()
					if ch in letter:
						l = letter[ch]

						for x in range(0, 7):
							lines[x] += l[x]


				res = "\n"
				for x in range(0, 7):
					res += lines[x] + "\n"
				res += "\n"
				self.view.replace(edit, region, res)

				print(lines)

